def plot_weak_learner_boundary_and_weights(X, y, learner, iteration, sample_weights, cmap='Paired'):
    """
    Plots the decision boundary of a single weak learner with data point sizes
    proportional to their sample weights.
    """
    if learner.orientation_vector is None or learner.threshold is None: return

    cmap_obj = plt.get_cmap(cmap)
    xmin, xmax = X[:, 0].min() - 1, X[:, 0].max() + 1
    ymin, ymax = X[:, 1].min() - 1, X[:, 1].max() + 1

    # Create a mesh to plot decision boundary
    xx, yy = np.meshgrid(np.linspace(xmin, xmax, 100), np.linspace(ymin, ymax, 100))
    Z = learner.predict(np.c_[xx.ravel(), yy.ravel()]).reshape(xx.shape)

    fig, ax = plt.subplots(figsize=(8, 6))

    # Plot the decision region contour
    ax.contourf(xx, yy, Z, levels=[-1.1, 0, 1.1], colors=['#F8B6B6', '#A6CCF5'], alpha=0.6)
    ax.contour(xx, yy, Z, levels=[0], colors='k', linewidths=1.5)

    # Plot data points, with size proportional to sample weights
    if np.max(sample_weights) > 0:
        s_sizes = 10 + 190 * (sample_weights / np.max(sample_weights))
    else:
        s_sizes = 50 * np.ones_like(sample_weights)

    scatter = ax.scatter(X[:, 0], X[:, 1], c=y, cmap=cmap_obj, edgecolors='k', s=s_sizes, linewidths=0.5)

    # Plot the linear separating line
    if np.abs(learner.orientation_vector[1]) > 1e-10:
        x_plot = np.array([xmin, xmax])
        # Line equation: y = (-p_x/p_y) * x + (threshold/p_y)
        y_plot = (-learner.orientation_vector[0] / learner.orientation_vector[1]) * x_plot + \
                 (learner.threshold / learner.orientation_vector[1])
        ax.plot(x_plot, y_plot, 'k--', linewidth=2, label='Weak Learner Boundary')
    elif np.abs(learner.orientation_vector[0]) > 1e-10:
        # Vertical line
        ax.axvline(x=learner.threshold / learner.orientation_vector[0], color='k', linestyle='--', linewidth=2, label='Weak Learner Boundary')

    ax.set_title(f"Weak Learner {iteration+1} Boundary (Weights for Iteration {iteration+1})")
    ax.set_xlabel('Feature 1 ($x_1$)')
    ax.set_ylabel('Feature 2 ($x_2$)')
    ax.grid(True, linestyle='--', alpha=0.6)
    ax.legend(handles=scatter.legend_elements()[0], labels=['Class -1', 'Class +1']) # Simplified legend
    plt.tight_layout()
    plt.show()

