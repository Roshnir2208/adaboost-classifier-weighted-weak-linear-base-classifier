# --- 1. Weighted Weak Linear Classifier Class ---
class WeightedWeakLinearClassifier:
    """
    A weak learner inspired by linear separation based on weighted class means,
    optimally selecting the threshold for minimal weighted error (stump approach).
    """
    def __init__(self):
        self.orientation_vector = None
        self.threshold = None
        self.polarity = 1

    def fit(self, X, y, sample_weights):
        """Trains the weak classifier by finding the optimal hyperplane."""

        positive_class_indices = (y == 1)
        negative_class_indices = (y == -1)

        positive_class = X[positive_class_indices]
        negative_class = X[negative_class_indices]
        positive_weights = sample_weights[positive_class_indices]
        negative_weights = sample_weights[negative_class_indices]

        # --- Robust Weighted Mean Calculation ---
        w_pos_sum = np.sum(positive_weights)
        w_neg_sum = np.sum(negative_weights)

        # Compute weighted class means (Fall back to unweighted if sum of weights is near zero)
        if w_pos_sum > 1e-10 and len(positive_class) > 0:
            mean_positive = np.average(positive_class, axis=0, weights=positive_weights)
        elif len(positive_class) > 0:
            mean_positive = np.mean(positive_class, axis=0)
        else:
             mean_positive = np.zeros(X.shape[1])

        if w_neg_sum > 1e-10 and len(negative_class) > 0:
            mean_negative = np.average(negative_class, axis=0, weights=negative_weights)
        elif len(negative_class) > 0:
            mean_negative = np.mean(negative_class, axis=0)
        else:
             mean_negative = np.zeros(X.shape[1])

        # Orientation vector (p)
        orientation = mean_positive - mean_negative
        norm = np.linalg.norm(orientation)

        # Normalise the orientation vector (p)
        if norm > 1e-10:
            self.orientation_vector = orientation / norm
        else:
            # If means coincide, choose an arbitrary direction (e.g., first axis)
            self.orientation_vector = np.array([1.0] + [0.0] * (X.shape[1] - 1))

        # Project all points onto the orientation vector
        projected_data = X @ self.orientation_vector

        # --- Robust Threshold Search (1D Stump) ---
        minimum_error = float('inf')
        best_threshold = None
        best_polarity = 1

        unique_projections = np.unique(projected_data)
        candidate_thresholds = []
        if len(unique_projections) > 1:
            # Midpoints between unique projected points
            candidate_thresholds.extend((unique_projections[:-1] + unique_projections[1:]) / 2)

        # Add extreme points for boundary checks
        candidate_thresholds.append(unique_projections[0] - 1.0)
        candidate_thresholds.append(unique_projections[-1] + 1.0)

        candidate_thresholds = np.unique(candidate_thresholds)

        for threshold in candidate_thresholds:
            for polarity in [1, -1]:
                # Polarity 1: If projection >= threshold, predict 1, else -1
                if polarity == 1:
                    predictions = np.where(projected_data >= threshold, 1, -1)
                # Polarity -1: If projection < threshold, predict 1, else -1
                else:
                    predictions = np.where(projected_data < threshold, 1, -1)

                # Weighted misclassification error
                error = np.sum(sample_weights[predictions != y])

                if error < minimum_error:
                    minimum_error = error
                    best_threshold = threshold
                    best_polarity = polarity

        self.threshold = best_threshold
        self.polarity = best_polarity
        return minimum_error

    def predict(self, X):
        """Makes predictions for a given set of features."""
        if self.orientation_vector is None or self.threshold is None:
             # Should only happen if the weak learner was not successfully fitted
             return np.zeros(X.shape[0])

        projected_data = X @ self.orientation_vector

        if self.polarity == 1:
            # Class +1 if projected_data >= threshold
            return np.where(projected_data >= self.threshold, 1, -1)
        else:
            # Class +1 if projected_data < threshold
            return np.where(projected_data < self.threshold, 1, -1)

class AdaBoost:
    """Implements the AdaBoost algorithm using custom weak learners."""
    def __init__(self, num_learners,early_stopping=False, tol=1.0):
        self.num_learners = num_learners
        self.weak_learners = []
        self.learner_weights = []
        self.errors_history = []
        self.sample_weights_history = []
        self.early_stopping = early_stopping
        self.tol = tol

    def fit(self, features, labels):
        num_samples = features.shape[0]
        self.weak_learners = []
        self.learner_weights = []
        self.errors_history = []
        self.sample_weights_history = []

        # Initialise uniform sample weights
        sample_weights = np.ones(num_samples) / num_samples

        for i in range(self.num_learners):
            weak_learner = WeightedWeakLinearClassifier()
            error = weak_learner.fit(features, labels, sample_weights)

            # Store the error before checks
            self.errors_history.append(error)

            # --- Early Stopping Checks ---
            if error < 1e-10:
                print(f"Perfect classification (error=0) achieved at iteration {i+1}.")
                learner_weight = 0.5 * np.log((1 - 1e-10) / 1e-10)
                self.weak_learners.append(weak_learner)
                self.learner_weights.append(learner_weight)
                self.sample_weights_history.append(sample_weights.copy())
                self.num_learners = i + 1
                break
            elif error >= 0.5:
                print(f"Weak learner error >= 0.5 at iteration {i+1}. Stopping.")
                self.num_learners = i
                break

            # Compute learner weight (alpha_t)
            learner_weight = 0.5 * np.log((1 - error) / error)
            predictions = weak_learner.predict(features)

            # Store sample weights *before* update (weights used for this iteration)
            self.sample_weights_history.append(sample_weights.copy())

            # Weight update rule
            sample_weights *= np.exp(-learner_weight * labels * predictions)
            sample_weights /= np.sum(sample_weights)  # Normalize

            self.weak_learners.append(weak_learner)
            self.learner_weights.append(learner_weight)
            if self.early_stopping:
              # Compute strong classifier prediction so far
              current_pred = self.predict(features)
              accuracy = np.mean(current_pred == labels)
              print(f"Iteration {i+1}, training accuracy: {accuracy*100:.2f}%")
            # Check 100% training accuracy to determine final 'n'
            if np.mean(self.predict(features) == labels) == 1.0:
                print(f"100% training accuracy achieved at iteration {i+1}")
                self.num_learners = i + 1
                break

        self.num_learners = len(self.weak_learners)

    def predict(self, features):
        """Strong classifier prediction: H(x) = sign(sum(alpha_t * h_t(x)))"""
        if not self.weak_learners:
             return np.zeros(features.shape[0])

        final_prediction = np.zeros(features.shape[0])
        for learner, weight in zip(self.weak_learners, self.learner_weights):
            final_prediction += weight * learner.predict(features)
        return np.sign(final_prediction)

    def staged_predict(self, features):
      """Returns a list of predictions after each learner is added."""
      cumulative_prediction = np.zeros(features.shape[0])
      predictions_list = []
      for alpha, learner in zip(self.learner_weights, self.weak_learners):
        cumulative_prediction += alpha * learner.predict(features)
        predictions_list.append(np.sign(cumulative_prediction.copy()))
      return predictions_list

