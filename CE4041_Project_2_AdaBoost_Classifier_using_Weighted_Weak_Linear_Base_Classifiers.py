def load_data(filename):
    """Loads the features (X) and labels (y) from a text file."""
    if not os.path.exists(filename):
        print(f"Error: File not found: {filename}")
        return np.array([]), np.array([])

    data = np.loadtxt(filename)
    X = data[:, :-1]  # Features
    y = data[:, -1]   # Labels
    return X, y

def plot_decision_boundary(X, y, model, title, steps=200, cmap='cividis'):
    """Plots the decision boundary of the strong classifier."""
    if not model.weak_learners: return

    cmap = plt.get_cmap(cmap)
    xmin, xmax = X[:, 0].min() - 1, X[:, 0].max() + 1
    ymin, ymax = X[:, 1].min() - 1, X[:, 1].max() + 1
    x_span = np.linspace(xmin, xmax, steps)
    y_span = np.linspace(ymin, ymax, steps)
    xx, yy = np.meshgrid(x_span, y_span)

    grid_points = np.c_[xx.ravel(), yy.ravel()]
    labels = model.predict(grid_points)
    z = labels.reshape(xx.shape)

    fig, ax = plt.subplots(figsize=(8, 6))

    # Plot contour
    ax.contourf(xx, yy, z, levels=[-1.1, 0, 1.1], colors=['#F8B6B6', '#A6CCF5'], alpha=0.6)
    ax.contour(xx, yy, z, levels=[0], colors='k', linewidths=2)

    # Plot data points
    scatter = ax.scatter(X[:, 0], X[:, 1], c=y, cmap=cmap, edgecolors='k', linewidths=0.5, s=50)
    handles, default_labels = scatter.legend_elements()
    custom_labels = ['Class -1', 'Class +1']
    legend1 = ax.legend(handles,
                        custom_labels,
                        loc="upper right",
                        title="Classes")

    ax.add_artist(legend1)

    ax.set_title(title)
    ax.set_xlabel('Feature 1 ($x_1$)')
    ax.set_ylabel('Feature 2 ($x_2$)')
    ax.grid(True, linestyle='--', alpha=0.6)
    plt.tight_layout()
    plt.show()

